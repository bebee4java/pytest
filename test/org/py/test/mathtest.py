import math, random

print(abs(2))     # 返回数字的绝对值
print(math.ceil(3.9))   # 返回一个大于或等于 x 的的最小整数
a = {1, 2, 8, 3, 3, 2, 9}
print(max(a))
print(min(a))
print(math.sqrt(8))
print(random.choice(range(10)))     # 从序列的元素中随机挑选一个元素
print(random.random())      # 随机生成下一个实数，它在[0,1)范围内
print(math.pi)
print(math.e)
print('44s4'.isdecimal())



